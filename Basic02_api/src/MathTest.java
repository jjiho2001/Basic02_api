
public class MathTest {

	public static void main(String[] args) {
		
		System.out.println("절대값 -> " + Math.abs(-9));
		System.out.println("올림 -> " + Math.ceil(9.5));
		System.out.println("버림 -> " + Math.floor(9.5));
		System.out.println("반올림 -> " + Math.round(9.5));
		System.out.println("max -> " + Math.max(9.5, -9.5));
		System.out.println("min -> " + Math.min(9.5, -9.5));
		System.out.println("pow -> " + Math.pow(5, 2));
		System.out.println("sqrt -> " + Math.sqrt(5));
		System.out.println("random -> " + Math.random());
	}

}
